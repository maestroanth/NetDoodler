So.....What is NetDoodler?


Even though NetDoodler is still in its infancy, I would like NetDoodler to be a website that represents a uniquely talented team of freelance coders (who may or may not be fed up with their prior jobs) who want a hand at being their own bosses to tackle various and unusual technical projects together.  But right now, NetDoodler is just me.  Down the road, I hope to meet other talented free-lancers whose skills compliment well with mine that are also trying to make an honest buck.  Maybe if I become a successful enough free-lancer, I can directly hire other developers to help.  If you want to know more about me, feel free to check out my LinkedIn page.

There are many other web building options from well established companies, what makes NetDoodler better than the rest of them?

The lack of tyrannical dictatorship. Well, all kidding aside, I’m not going to lie to you, NetDoodler isn’t better than them, and they aren’t necessarily better than NetDoodler.  I hope that brute honesty just blew your mind.  In fact, it all depends on the perspective and philosophy of the client.  If you like creativity, honesty, originality, and professionals with a broad scope who can think outside the box, then NetDoodler is a great choice.  I (or “we”) will work hard for you.
Basically, NetDoodler is an mono e mono competition of my talent against the world.

What is your philosophy on web design?

I enjoy websites that can be a viewport to 'another universe' so to speak....and not just another hackneyed advertisement.  A website needs to be something your visitor will actually “remember” for a long time.  Sometimes it takes more than just a professional appearance to achieve this.

For example, when I envisioned "NetDoodler", I didn't want to create just another generic 'free-lancer' web developer site with my ‘purty’ picture, ‘purty’ fonts, ‘purty’ portfolio, irrelevant blogs, nice outdoorsy photos, etc. begging for your patronage that gets lost with all the other online garbage.  In short, I wanted a website where my visitors will "remember me"…..even if cheesy, cartoon crayons with a tinge of bloodlust isn't their thing. ;)

And likewise, I hope the work I do for my clients will make them be "remembered" as well!  I believe truly having memorable brand will sell better than a bunch of ‘purty’, stale ads.

Can you explain your creative thought process behind NetDoodler in more detail?

My first step was to create an aesthetic completely based on whatever domain name I could come up with that wasn’t already taken (unfortunately, WebDoodler was taken! Hah!).  Also, the look had to be logically consistent with that domain name.  For example, if you actually do go to the site, ‘webdoodler.com’ (dirty domain thieves ;P ), you’ll see that none of their design has nothing to do with the domain name, “webdoodler.com”.  There is not even a picture of a chalkboard doodle…… it’s just your stereotypical, generic “hire me” hype, with a bunch of dumb people smiling (apologies, “satisfied customers smiling!”) all slapped together in 2 hours with a general, bootstrap theme.  How can they even call themselves “WebDoodlers”! And of course, they probably charge clients a $1,000 for something similar….If I charge a client $1000, I can guarantee that it will be something truly awesome that competitors cannot even think of.

And I don’t just want to pick on these poor “WebDoodler” people, ALL website building business sites do this!

TLDR ;P But please continue……..

However, with NetDoodler, I wanted something different, memorable. So I rolled up my sleeves and I did all the extra work to make my vision come true.  Even when that required for me to improvise and pick up a few more skills that’s a little outside my scope.  This in turn required me to learn the entire adobe creative suite in about a month, as well as freshening up my drawing skills for all the various logos, characters, and ads for my brand.  In addition, I had to tap into some musical skills too for some original music composition and sound mixings since I wanted to make a cartoon!

If I were to hire you for one of my projects, what will that all involve?

Well, it will depend on the project. I’m flexible.  If it’s creating a website from scratch for you or your business, I’m hoping a similar creative process as I described just up above can work. (I.e. 1. Come up with a domain name, 2.  purchase it, 3. Brainstorm how the site would look with that domain name, 4. Design a Logo, 5. Pick a bootstrap theme to begin building, 6. Create some ad designs, etc.)

The more creative you want it; the more communication we will need.

Do you just build websites?

